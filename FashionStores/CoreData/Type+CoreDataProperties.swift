import Foundation
import CoreData


extension Type {

    @nonobjc public class func fetchRequest() -> NSFetchRequest<Type> {
        return NSFetchRequest<Type>(entityName: "Type")
    }

    @NSManaged public var typeID: String?
    @NSManaged public var name: String?
    @NSManaged public var store: Store?

}
